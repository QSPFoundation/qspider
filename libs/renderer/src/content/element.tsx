import { Attributes } from '@qspider/game-state';
import React from 'react';
import { useAttributes } from './attributes';
// import { useStyle } from '../hooks';

export interface ElementProps {
  attrs: Attributes;
  children?: React.ReactNode;
  tagName: string;
}

const voidTags = [
  'area',
  'base',
  'br',
  'col',
  'command',
  'embed',
  'hr',
  'img',
  'input',
  'keygen',
  'link',
  'meta',
  'param',
  'source',
  'track',
  'wbr',
];

export const TAGS_MAP: Record<string, string> = {
  altglyph: 'altGlyph',
  altglyphdef: 'altGlyphDef',
  altglyphitem: 'altGlyphItem',
  animatecolor: 'animateColor',
  animatemotion: 'animateMotion',
  animatetransform: 'animateTransform',
  clippath: 'clipPath',
  feblend: 'feBlend',
  fecolormatrix: 'feColorMatrix',
  fecomponenttransfer: 'feComponentTransfer',
  fecomposite: 'feComposite',
  feconvolvematrix: 'feConvolveMatrix',
  fediffuselighting: 'feDiffuseLighting',
  fedisplacementmap: 'feDisplacementMap',
  fedistantlight: 'feDistantLight',
  fedropshadow: 'feDropShadow',
  feflood: 'feFlood',
  fefunca: 'feFuncA',
  fefuncb: 'feFuncB',
  fefuncg: 'feFuncG',
  fefuncr: 'feFuncR',
  fegaussianblur: 'feGaussianBlur',
  feimage: 'feImage',
  femerge: 'feMerge',
  femergenode: 'feMergeNode',
  femorphology: 'feMorphology',
  feoffset: 'feOffset',
  fepointlight: 'fePointLight',
  fespecularlighting: 'feSpecularLighting',
  fespotlight: 'feSpotLight',
  fetile: 'feTile',
  feturbulence: 'feTurbulence',
  foreignobject: 'foreignObject',
  glyphref: 'glyphRef',
  lineargradient: 'linearGradient',
  radialgradient: 'radialGradient',
};

export const Element: React.FC<ElementProps> = ({ attrs = {}, children = null, tagName }) => {
  const tag = tagName in TAGS_MAP ? TAGS_MAP[tagName] : tagName;
  const [Tag, style, attributes] = useAttributes(attrs, tag as keyof JSX.IntrinsicElements);
  const selfClose = voidTags.includes(tagName);
  return selfClose ? (
    <Tag style={style} {...attributes} />
  ) : (
    <Tag style={style} {...attributes}>
      {children}
    </Tag>
  );
};
